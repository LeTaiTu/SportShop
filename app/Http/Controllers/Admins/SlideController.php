        return view('admin.slides.index', compact('slide'));
    }

    public function search(Request $req)
    {
        $slide = Slide::where('name_slide','like', "%".$req->txtsearch."%")
        ->orWhere('id_kind','like',"%".$req->txtsearch."%")
        ->orderBy('created_at', 'desc')->paginate(3);
        return view('admin.slides.index', compact('slide'));
    }
